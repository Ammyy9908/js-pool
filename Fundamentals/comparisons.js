/* Comparison opeartor in most programming languages
 are <,>,<=,>=,==,===,!=*/

// Comparison always return a boolean value

//example
console.log(2 > 1); //  true
console.log(10 < 20); // false

console.log(2 >= 2); // true

// String comparison

console.log("a" > "z"); // because "z" comes after "a" means "z" is greater than "a"

// comparision of different types

console.log("2" > 1); // true

console.log("01" == 1); // true because 01(binary)->1

// strict comparision

console.log("01" === 1); //false
